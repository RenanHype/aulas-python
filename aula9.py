#se/se não se/se não
entrada = input('você gosta de mim ou me ama? ')

if entrada == 'eu te amo':  
    print ('eu também te amo')
elif entrada == 'eu gosto de você':
    print ('eu também gosto de você')
elif entrada == ('eu te odeio'):
    print ('eu também te odeio')    
else: 
    print ('Ignorante')
